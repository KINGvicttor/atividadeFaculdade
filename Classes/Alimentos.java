package Classes;

public class Alimentos {

	//atributos
	private String nome, tipo;
	private int opção;
	//get e set
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getOpção() {
		return opção;
	}
	public void setOpção(int opção) {
		this.opção = opção;
	}
}
